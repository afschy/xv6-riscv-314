uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_trace(void) {
  int trace_index;
  argint(0, &trace_index);
  if(trace_index<=0) return -1;

  myproc()->trace_index = trace_index;
  return 0;
}

uint64
sys_history(void) {
  int index;
  argint(0, &index);
  uint64 stat;
  argaddr(1, &stat);

  struct syscall_stat *fetched = get_stat(index);
  acquire(&(fetched->lock));
  copyout(myproc()->pagetable, stat, (char *)fetched, sizeof(*fetched));
  release(&(fetched->lock));
  return 0;
}

uint64 sys_shutdown(void) {
  set_halt_flag();
  return 0;
}